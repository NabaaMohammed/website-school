<h2>PRIMARY SCHOOL</h2>
        EDUCATION IN ELEMENTARY SCHOOL
In Ashk Baghdad Schools, kids learn everything from the best teacher of the world, nature itself.
Ashk Baghdad Schools which methodizes learning by doing and living mentality on education taking down the classical patterns, and defines success as education is life; raises its students not only academically, but also on all fields and prepares them to life. Enhancing their mental and physical skills, presents them an active education experience. Nature Themed Education Model grounds on the truth that each student is a different individual and is based on the logic that observe the world from a different viewpoint and widen his/her horizon with that viewpoint. In Project Based Education Model, education mentality is not static, but rather dynamic and modernist. Because Ashk Baghdad's foundation is innovation and innovativeness shows itself in all academic and social platforms



